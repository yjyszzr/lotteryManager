package com.fh.config;

public class URLConfig {
	
	private String serviceSmsUrl;
	private String imgShowUrl;
	private String manualAuditUrl;
	private String manualRewardUrl;
	private String uploadURL;
	
	
	
	public String getUploadURL() {
		return uploadURL;
	}
	public void setUploadURL(String uploadURL) {
		this.uploadURL = uploadURL;
	}
	public String getServiceSmsUrl() {
		return serviceSmsUrl;
	}
	public void setServiceSmsUrl(String serviceSmsUrl) {
		this.serviceSmsUrl = serviceSmsUrl;
	}
	public String getImgShowUrl() {
		return imgShowUrl;
	}
	public void setImgShowUrl(String imgShowUrl) {
		this.imgShowUrl = imgShowUrl;
	}
	public String getManualAuditUrl() {
		return manualAuditUrl;
	}
	public void setManualAuditUrl(String manualAuditUrl) {
		this.manualAuditUrl = manualAuditUrl;
	}
	public String getManualRewardUrl() {
		return manualRewardUrl;
	}
	public void setManualRewardUrl(String manualRewardUrl) {
		this.manualRewardUrl = manualRewardUrl;
	}
	
	

}
